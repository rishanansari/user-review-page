//Select user target element
const img=document.getElementById("person-img");
const author=document.getElementById("author");
const job=document.getElementById("job");
const info=document.getElementById("info");


//lets select button
const prevBtn=document.querySelector(".prev-btn");
const nextBtn=document.querySelector(".next-btn");
const randomBtn=document.querySelector(".random-btn");


//Try to load the data for first time 
let currentItem=0;
window.addEventListener("DOMContentLoaded",()=>{
   const item=reviews[currentItem];//first data of the array of objects
   console.log(item);
   img.src=item.img;
   author.innerHTML=item.name;
   job.innerHTML=item.job;
   info.innerHTML=item.text;
})

//create a function showUserReview based on button 
//is to print and load other data infor
function showUserReview(person){
    const item=reviews[person];//reviews[0]
    console.log(currentItem);
   img.src=item.img;
   author.innerHTML=item.name;
   job.innerHTML=item.job;
   info.innerHTML=item.text;
}


//Logic for next user when user clicks on button
nextBtn.addEventListener("click",()=>{
    //increment the current position +1
    currentItem++;//20
    //is user reached to the end of the object or last ->it should be reset to 0
    // 20>19
    if(currentItem>reviews.length-1){
        currentItem=0;
    }
    showUserReview(currentItem);
})

//Logic for previous user when user click on button 
prevBtn.addEventListener("click", ()=>{
    currentItem--;

    if(currentItem<0){
        currentItem=reviews.length-1;//point current item to last index if user <0
    }

    showUserReview(currentItem);
})


//get the logic of random user
randomBtn.addEventListener("click",()=>{
    currentItem=Math.trunc(Math.random()*reviews.length);
    console.log(currentItem);
    showUserReview(currentItem);
});